package com.company;

public class MathOperation {
    int sum(int x, int y) {
        int result = x + y;
        return result;
    }

    float substract(float a, float b) {
        float result = a - b;
        return result;
    }

    float multiplication(float c, float d) {
        float result = c * d;
        return result;
    }

    double divide(double x, double y) {
        double result = x / y;
        return result;
    }

    float modulo(float x, float y) {
        float result = x % y;
        return result;
    }
}
